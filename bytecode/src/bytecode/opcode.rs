impl OpCode {
    pub fn from_u8(byte: u8) -> Option<Self> {
        if byte <= Self::StringForLoop as u8 {
            Some(unsafe { std::mem::transmute::<u8, OpCode>(byte) })
        } else {
            None
        }
    }
}
